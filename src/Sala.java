public class Sala {

    int idsala;
    String descricao;

    public Sala(int idsala, String descricao) {
        this.idsala = idsala;
        this.descricao = descricao;
    }

    public int getIdsala() {
        return idsala;
    }

    public String getDescricao() {
        return descricao;
    }
}
